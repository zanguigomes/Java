    private void btnCadDepartamentoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadDepartamentoSalvarActionPerformed
        dpto.setNome(txtCadDepartamentoNome.getText());
        dpto.setCod(txtCadDepartamentoCodigo.getText());
        dpto.setNotas(txtCadDepartamentoNotas.getText());

        //Certifica que os campos obrigatórios não estão vazios
        if(txtCadDepartamentoNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o Nome para prosseguir");
            txtCadDepartamentoNome.requestFocus();
        }else if(txtCadDepartamentoCodigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o Código para prosseguir");
            txtCadDepartamentoCodigo.requestFocus();
        }else{
            if(opcControl == 1){
                //Grava efectivamente os dados no banco
                controle.gravarDepartamento(dpto);
                //Actualiza a tabela que lista os usuarios cadastrados
                preencheTabela("SELECT departamentos.dptoNome,departamentos.dptoCod,usuarios.userNome,departamentos.dptoDesc FROM departamentos INNER JOIN usuarios ON usuarios.userId = departamentos.dptoDiret");
            }else{
                dpto.setId(Integer.parseInt(txtCadDepartamentoId.getText()));
                controle.editarDepartamento(dpto);
                //Actualiza a tabela que lista os usuarios cadastrados
                preencheTabela("SELECT departamentos.dptoNome,departamentos.dptoCod,usuarios.userNome,departamentos.dptoDesc FROM departamentos INNER JOIN usuarios ON usuarios.userId = departamentos.dptoDiret");
            }
            //Após gravar limpa os campos para a próxima inserçao
            txtCadDepartamentoNome.setText("");
            txtCadDepartamentoNotas.setText("");

            //Torna os campos inativos novamente
            txtCadDepartamentoNome.setEnabled(false);
            txtCadDepartamentoNotas.setEnabled(false);
            btnCadDepartamentoSalvar.setEnabled(false);
            btnCadDepartamentoCancelar.setEnabled(false);
        }
    }//GEN-LAST:event_btnCadDepartamentoSalvarActionPerformed

