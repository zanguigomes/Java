    private void jTableDepartamentoResultMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableDepartamentoResultMouseClicked
        // Ao seleccionar um item na tabela redireciona para o registo correspondente
        int stt = 1;
        String dp = ""+jTableDepartamentoResult.getValueAt(jTableDepartamentoResult.getSelectedRow(), 1);
        conexTb.conexao();
        conexTb.executaSQL("SELECT * FROM departamentos WHERE dptoCod ='"+dp+"'");
        try {
            conexTb.res.first();
            txtCadDepartamentoId.setText(String.valueOf(conexTb.res.getInt("dptoId")));
            txtCadDepartamentoNome.setText(conexTb.res.getString("dptoNome"));
            txtCadDepartamentoCodigo.setText(conexTb.res.getString("dptoCod"));
            txtCadDepartamentoNotas.setText(conexTb.res.getString("dptoDesc"));
            ModuloConexao sellConex = new ModuloConexao();
            sellConex.conexao();
            sellConex.executaSQL("SELECT * FROM usuarios WHERE userId = '"+conexTb.res.getInt("dptoDiret")+"'");
            sellConex.res.first();
            comboCadDepartamentoDiret.setSelectedItem(sellConex.res.getString("userNome"));
            sellConex.desconetar();
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro: Não foi possível listar Departamentos \n"+ex.getLocalizedMessage());
        }
        conexTb.desconetar();
        btnCadDepartamentoEditar.setEnabled(true);
        btnCadDepartamentoExcluir.setEnabled(true);
    }//GEN-LAST:event_jTableDepartamentoResultMouseClicked
//Métodompara preencher a tabela resultado da pesquisa
    public void preencheTabela(String Sql){
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"Nome","Códio","Director","Notas"};
        conex.conexao();
        conex.executaSQL(Sql);
        try {
            conex.res.first();
            do{
                dados.add(new Object[]{
                    conex.res.getString("dptoNome"),
                    conex.res.getString("dptoCod"),
                    conex.res.getString("userNome"),
                    conex.res.getString("dptoDesc")
                });
            }while(conex.res.next());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
        ModeloTabela tabela = new ModeloTabela(dados, colunas);
        jTableDepartamentoResult.setModel(tabela);
        jTableDepartamentoResult.getColumnModel().getColumn(0).setPreferredWidth(200);
        jTableDepartamentoResult.getColumnModel().getColumn(0).setResizable(false);
        jTableDepartamentoResult.getColumnModel().getColumn(1).setPreferredWidth(100);
        jTableDepartamentoResult.getColumnModel().getColumn(1).setResizable(false);
        jTableDepartamentoResult.getColumnModel().getColumn(2).setPreferredWidth(200);
        jTableDepartamentoResult.getColumnModel().getColumn(2).setResizable(false);
        jTableDepartamentoResult.getColumnModel().getColumn(3).setPreferredWidth(240);
        jTableDepartamentoResult.getColumnModel().getColumn(3).setResizable(false);
        
        jTableDepartamentoResult.getTableHeader().setReorderingAllowed(false);
        jTableDepartamentoResult.setAutoResizeMode(jTableDepartamentoResult.AUTO_RESIZE_OFF);
        jTableDepartamentoResult.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconetar();
    }
