    private void btnCadUsuarioSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadUsuarioSalvarActionPerformed
        //Verifica usuario no banco de dados
        //        modelo.setEmail(txtCadUsuarioEmail.getText());
        //        modelo.setLogin(txtCadUsuarioLogin.getText());
        //        controle.verificaUsuario(modelo);

        //Grava as informações no banco de dados
        //modelo.setCodigo(Integer.parseInt(txtCadUsuarioId.getText()));
        
        user.setLogin(txtCadUsuarioLogin.getText());
        user.setSenha(txtCadUsuarioSenha.getText());
        user.setNome(txtCadUsuarioNome.getText());
        user.setEmail(txtCadUsuarioEmail.getText());
        user.setDepartamento((String) comboCadUsuarioDepartamento.getSelectedItem());
        user.setNivel(comboCadUsuarioNivel.getSelectedItem().toString());
        user.setNotas(txtCadUsuarioNotas.getText());
        user.setStatus((String) comboCadUsuarioStatus.getSelectedItem());
        
        //Certifica que os campos obrigatórios não estão vazios
        if(txtCadUsuarioLogin.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o Código para prosseguir");
            txtCadUsuarioLogin.requestFocus();
        }else if(txtCadUsuarioSenha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha a Senha para prosseguir");
            txtCadUsuarioSenha.requestFocus();
        }else if(txtCadUsuarioConfirmaSenha.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Confirme a Senha para prosseguir");
            txtCadUsuarioConfirmaSenha.requestFocus();
        }else if((txtCadUsuarioSenha.getText()) == null ? (txtCadUsuarioConfirmaSenha.getText()) != null : !(txtCadUsuarioSenha.getText()).equals(txtCadUsuarioConfirmaSenha.getText())){
            JOptionPane.showMessageDialog(null, "As Senhas não conferem. Digite novamente.");
            txtCadUsuarioSenha.requestFocus();
        }else if(txtCadUsuarioNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o Nome para prosseguir");
            txtCadUsuarioNome.requestFocus();
        }else if(txtCadUsuarioEmail.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o Email para prosseguir");
            txtCadUsuarioEmail.requestFocus();
        }else{
            if(opcControl == 1){
                //Grava efectivamente os dados no banco
                controle.gravarUsuario(user);
                //Actualiza a tabela que lista os usuarios cadastrados
                preencheTabela("SELECT userLogin, userNome, dptoNome, userNivel, userStatus, userCadNotas FROM usuarios INNER JOIN departamentos on userDptoId = dptoId ORDER BY userId DESC");
            }else{
                user.setCodigo(Integer.parseInt(txtCadUsuarioId.getText()));
                controle.editarUsuario(user);
                //Actualiza a tabela que lista os usuarios cadastrados
                preencheTabela("SELECT userLogin, userNome, dptoNome, userNivel, userStatus, userCadNotas FROM usuarios INNER JOIN departamentos on userDptoId = dptoId ORDER BY userId DESC");
            }
            //Após gravar limpa os campos para a próxima inserçao
            txtCadUsuarioLogin.setText("");
            txtCadUsuarioSenha.setText("");
            txtCadUsuarioConfirmaSenha.setText("");
            txtCadUsuarioNome.setText("");
            txtCadUsuarioEmail.setText("");
            txtCadUsuarioNotas.setText("");

            //Torna os campos inativos novamente
            txtCadUsuarioLogin.setEnabled(false);
            txtCadUsuarioSenha.setEnabled(false);
            txtCadUsuarioConfirmaSenha.setEnabled(false);
            txtCadUsuarioNome.setEnabled(false);
            txtCadUsuarioEmail.setEnabled(false);
            comboCadUsuarioDepartamento.setEnabled(false);
            comboCadUsuarioNivel.setEnabled(false);
            comboCadUsuarioStatus.setEnabled(false);
            txtCadUsuarioNotas.setEnabled(false);
            btnCadUsuarioSalvar.setEnabled(false);
            btnCadUsuarioCancelar.setEnabled(false);
        }

    }//GEN-LAST:event_btnCadUsuarioSalvarActionPerformed

