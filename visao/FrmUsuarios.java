    //Método para preencher a tabela resultado da pesquisa
    public void preencheTabela(String Sql){
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"Código","Nome","Departamento","Perfil","Status","Notas"};
        conex.conexao();
        conex.executaSQL(Sql);
        try {
            conex.res.first();
            do{
                dados.add(new Object[]{
                    conex.res.getString("userLogin"),
                    conex.res.getString("userNome"),
                    conex.res.getString("dptoNome"),
                    conex.res.getString("userNivel"),
                    conex.res.getString("userStatus"),
                    conex.res.getString("userCadNotas")
                });
            }while(conex.res.next());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
        ModeloTabela tabela = new ModeloTabela(dados, colunas);
        jTableUsuariosResult.setModel(tabela);
        jTableUsuariosResult.getColumnModel().getColumn(0).setPreferredWidth(100);
        jTableUsuariosResult.getColumnModel().getColumn(0).setResizable(false);
        jTableUsuariosResult.getColumnModel().getColumn(1).setPreferredWidth(100);
        jTableUsuariosResult.getColumnModel().getColumn(1).setResizable(false);
        jTableUsuariosResult.getColumnModel().getColumn(2).setPreferredWidth(130);
        jTableUsuariosResult.getColumnModel().getColumn(2).setResizable(false);
        jTableUsuariosResult.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTableUsuariosResult.getColumnModel().getColumn(3).setResizable(false);
        jTableUsuariosResult.getColumnModel().getColumn(4).setPreferredWidth(100);
        jTableUsuariosResult.getColumnModel().getColumn(4).setResizable(false);
        jTableUsuariosResult.getColumnModel().getColumn(5).setPreferredWidth(250);
        jTableUsuariosResult.getColumnModel().getColumn(5).setResizable(false);
        jTableUsuariosResult.getTableHeader().setReorderingAllowed(false);
        jTableUsuariosResult.setAutoResizeMode(jTableUsuariosResult.AUTO_RESIZE_OFF);
        jTableUsuariosResult.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconetar();
    }
    
    
