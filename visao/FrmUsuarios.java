    private void jTableUsuariosResultMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableUsuariosResultMouseClicked
        // Ao seleccionar um item na tabela redireciona para o registo correspondente
        
        String usuario = ""+jTableUsuariosResult.getValueAt(jTableUsuariosResult.getSelectedRow(), 1);
        conex.conexao();
        conex.executaSQL("SELECT * FROM usuarios WHERE userNome ='"+usuario+"'");
        try {
            conex.res.first();
            txtCadUsuarioId.setText(String.valueOf(conex.res.getInt("userId")));
            txtCadUsuarioLogin.setText(conex.res.getString("userLogin"));
            txtCadUsuarioSenha.setText(conex.res.getString("userSenha"));
            txtCadUsuarioNome.setText(conex.res.getString("userNome"));
            txtCadUsuarioEmail.setText(conex.res.getString("userEmail"));
            comboCadUsuarioNivel.setSelectedItem(conex.res.getString("userNivel"));
            comboCadUsuarioStatus.setSelectedItem(conex.res.getString("userStatus"));
            txtCadUsuarioNotas.setText(conex.res.getString("userCadNotas"));
            ModuloConexao sellConex = new ModuloConexao();
            sellConex.conexao();
            sellConex.executaSQL("SELECT * FROM departamentos WHERE dptoId = '"+conex.res.getInt("userDptoId")+"'");
            sellConex.res.first();
            comboCadUsuarioDepartamento.setSelectedItem(sellConex.res.getString("dptoNome"));
            sellConex.desconetar();
        } catch (SQLException ex) {
            Logger.getLogger(FrmUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
        conex.desconetar();
        btnCadUsuarioEditar.setEnabled(true);
        btnCadUsuarioExcluir.setEnabled(true);
    }//GEN-LAST:event_jTableUsuariosResultMouseClicked

