    public void listaDepartamentos(){
        conex.conexao();
        conex.executaSQL("SELECT * FROM departamentos ORDER BY dptoNome");
        try {
            conex.res.first();
            comboCadUsuarioDepartamento.removeAllItems();
            do{
              comboCadUsuarioDepartamento.addItem(conex.res.getString("dptoNome"));
            }while(conex.res.next());                   
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível listar os departamentos");
        }
        conex.desconetar();
    }
