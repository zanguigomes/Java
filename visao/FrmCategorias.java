    private void btnCadCategoriaExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadCategoriaExcluirActionPerformed
        // Exclui uma categoria
        int alerta = 0;
        alerta = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente excluir esta Categoria?");
        if(alerta == JOptionPane.YES_OPTION){
            cat.setId(Integer.parseInt(txtCadCategoriaId.getText()));
            controle.eliminarCategoria(cat);
        }
        //Após eliminar limpa os campos do formulário
        txtCadCategoriaId.setText("");
        txtCadCategoriaNome.setText("");
        txtCadCategoriaNotas.setText("");
        txtCadCategoriaPesquisar.setText("");
        //Habilita/desbilita alguns botões
        btnCadCategoriaNovo.setEnabled(true);
        btnCadCategoriaEditar.setEnabled(false);
        btnCadCategoriaExcluir.setEnabled(false);
        //Actualiza a tabela que lista os usuarios cadastrados
        preencheTabela("SELECT * FROM categorias ORDER BY catId DESC");
    }//GEN-LAST:event_btnCadCategoriaExcluirActionPerformed

