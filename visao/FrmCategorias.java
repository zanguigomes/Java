    private void btnCadCategoriaSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadCategoriaSalvarActionPerformed
        cat.setNome(txtCadCategoriaNome.getText());
        cat.setNotas(txtCadCategoriaNotas.getText());
        cat.setStatus(Integer.parseInt(txtCategoriaStatus.getText()));

        //Certifica que os campos obrigatórios não estão vazios
        if(txtCadCategoriaNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha o Nome para prosseguir");
            txtCadCategoriaNome.requestFocus();
        }else{
            if(opcControl == 1){
                //Grava efectivamente os dados no banco
                controle.gravarCategoria(cat);
                //Actualiza a tabela que lista os usuarios cadastrados
                preencheTabela("SELECT * FROM categorias ORDER BY catId DESC");
            }else{
                cat.setId(Integer.parseInt(txtCadCategoriaId.getText()));
                controle.editarCategoria(cat);
                //Actualiza a tabela que lista os usuarios cadastrados
                preencheTabela("SELECT * FROM categorias ORDER BY catId DESC");
            }
            //Após gravar limpa os campos para a próxima inserçao
            txtCadCategoriaNome.setText("");
            txtCadCategoriaNotas.setText("");

            //Torna os campos inativos novamente
            txtCadCategoriaNome.setEnabled(false);
            txtCadCategoriaNotas.setEnabled(false);
            btnCadCategoriaSalvar.setEnabled(false);
            btnCadCategoriaCancelar.setEnabled(false);
        }
    }//GEN-LAST:event_btnCadCategoriaSalvarActionPerformed

