    //Método para preencher a tabela resultado da pesquisa
    public void preencheTabela(String Sql){
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"Código","Nome","Notas","Status"};
        conex.conexao();
        conex.executaSQL(Sql);
        try {
            conex.res.first();
            do{
                dados.add(new Object[]{
                    conex.res.getInt("catId"),
                    conex.res.getString("catNome"),
                    conex.res.getString("catDesc"),
                    conex.res.getString("catStatus")
                });
            }while(conex.res.next());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
        ModeloTabela tabela = new ModeloTabela(dados, colunas);
        jTableCategoriaResult.setModel(tabela);
        jTableCategoriaResult.getColumnModel().getColumn(0).setPreferredWidth(50);
        jTableCategoriaResult.getColumnModel().getColumn(0).setResizable(false);
        jTableCategoriaResult.getColumnModel().getColumn(1).setPreferredWidth(200);
        jTableCategoriaResult.getColumnModel().getColumn(1).setResizable(false);
        jTableCategoriaResult.getColumnModel().getColumn(2).setPreferredWidth(350);
        jTableCategoriaResult.getColumnModel().getColumn(2).setResizable(false);
        jTableCategoriaResult.getColumnModel().getColumn(3).setPreferredWidth(100);
        jTableCategoriaResult.getColumnModel().getColumn(3).setResizable(false);
        jTableCategoriaResult.getTableHeader().setReorderingAllowed(false);
        jTableCategoriaResult.setAutoResizeMode(jTableCategoriaResult.AUTO_RESIZE_OFF);
        jTableCategoriaResult.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconetar();
    }
    
    
    
